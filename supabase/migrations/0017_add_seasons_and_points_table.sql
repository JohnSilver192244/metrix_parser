-- Story rating-phase setup
-- Purpose: introduce explicit season configuration and a season points matrix
-- (placement x players_count) and seed RDGA season 2026 from the provided CSV table.

create table if not exists app_public.seasons (
  id bigint generated by default as identity primary key,
  season_code text not null,
  name text not null,
  date_from date not null,
  date_to date not null,
  best_leagues_count integer not null default 4,
  best_tournaments_count integer not null default 4,
  min_players integer not null default 8,
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now()),
  constraint seasons_season_code_key unique (season_code),
  constraint seasons_name_check check (char_length(btrim(name)) > 0),
  constraint seasons_date_range_check check (date_to >= date_from),
  constraint seasons_best_leagues_count_check check (best_leagues_count >= 0),
  constraint seasons_best_tournaments_count_check check (best_tournaments_count >= 0),
  constraint seasons_min_players_check check (min_players > 0)
);

create table if not exists app_public.season_points_table (
  id bigint generated by default as identity primary key,
  season_code text not null,
  players_count integer not null,
  placement integer not null,
  points numeric(10, 2) not null,
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now()),
  constraint season_points_table_season_players_placement_key
    unique (season_code, players_count, placement),
  constraint season_points_table_season_code_fkey
    foreign key (season_code) references app_public.seasons (season_code)
    on delete cascade,
  constraint season_points_table_players_count_check check (players_count >= 8),
  constraint season_points_table_placement_check check (placement > 0),
  constraint season_points_table_points_check check (points >= 0),
  constraint season_points_table_placement_players_bound_check
    check (placement <= players_count)
);

create index if not exists season_points_table_season_players_idx
  on app_public.season_points_table (season_code, players_count, placement);

