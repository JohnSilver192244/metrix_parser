-- Story 3.1
-- Purpose: establish the schema needed for later player/result persistence
-- while Epic 3 still focuses only on selecting competitions and fetching raw
-- result payloads.

create table if not exists app_public.players (
  id bigint generated by default as identity primary key,
  player_id text not null,
  player_name text not null,
  raw_payload jsonb,
  source_fetched_at timestamptz,
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now()),
  constraint players_player_id_key unique (player_id)
);

create table if not exists app_public.competition_results (
  id bigint generated by default as identity primary key,
  competition_id text not null,
  player_id text not null,
  class_name text,
  sum integer,
  diff integer,
  order_number integer,
  dnf boolean not null default false,
  raw_payload jsonb,
  source_fetched_at timestamptz,
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now()),
  constraint competition_results_competition_player_order_key unique (competition_id, player_id, order_number),
  constraint competition_results_competition_id_fkey
    foreign key (competition_id) references app_public.competitions (competition_id),
  constraint competition_results_player_id_fkey
    foreign key (player_id) references app_public.players (player_id)
);

create index if not exists competition_results_competition_id_idx
  on app_public.competition_results (competition_id);

create index if not exists competition_results_player_id_idx
  on app_public.competition_results (player_id);
